def q(stem, opts, correct):
    """Build a Q. `correct` is A/B/C/D (the letter index into opts)."""
    assert correct in ("A", "B", "C", "D"), f"bad letter: {correct!r}"
    assert len(opts) == 4, f"need 4 options, got {len(opts)}: {opts}"
    assert len(set(opts)) == 4, f"duplicate options: {opts}"
    assert stem.strip(), "empty stem"
    return {"question": stem, "options": list(opts), "answer": correct}


